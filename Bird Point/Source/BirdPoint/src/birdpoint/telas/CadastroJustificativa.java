    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (Util.chkVazio(tfNomeProfessor.getText(), tfDataJustificativa.getText(), tfJustificativa.getText())) {
            dataAtual = new Date();
            ponto.setJustificativaPonto("(Justificado em: " + formatarData.format(dataAtual) + ") " + tfJustificativa.getText());
            ponto.setAtestadoPonto(jcAtestado.isSelected());
            if(pontoDAO.atualizar(ponto)){
               JOptionPane.showMessageDialog(rootPane, "Justificativa registrada com sucesso!");
            }else{
               JOptionPane.showMessageDialog(rootPane, "Não foi possível registrar a justificativa!"); 
            }
            btLimparActionPerformed(null);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

