    private void btPesqusar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesqusar1ActionPerformed
         if (Util.chkVazio(tfDataJustificativa.getText())) {
            List<Ponto> lista;
            lista = (pontoDAO.checkExistseq("dataPonto",tfDataJustificativa.getText()));
            List<Ponto> listaFiltrada = new ArrayList<>();
            for (Ponto ponto1 : lista) {
                if(ponto1.getHoraEntradaPonto()==null || ponto1.getHoraSaidaPonto()==null){
                    listaFiltrada.add(ponto1);
                }
            }
            PontoTableModel itm = new PontoTableModel(listaFiltrada);
            Object objetoRetorno = PesquisaGenerica.exibeTela(itm, "Ponto");
            if (objetoRetorno != null) {
                ponto = pontoDAO.consultarObjetoId("idPonto", objetoRetorno);
                tfNomeProfessor.setText(ponto.getProfessor().getNomeProfessor());
                tfJustificativa.setText(ponto.getJustificativaPonto());
                jcAtestado.setSelected(ponto.isAtestadoPonto());
                

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Informe a data que deseja justificar");
        } 
    }//GEN-LAST:event_btPesqusar1ActionPerformed

